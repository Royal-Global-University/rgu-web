                <div class="headd2 text-dark fw-bold pb-3 pt-2" style="line-height: 1.3;">Activity Report on "Lecture Discussion on LOCF"
                </div>

                <div class="para1 text-dark pb-3" style="text-align: justify;">A Lecture –Discussion program was organized by the IQAC on July 4, 2022, at the Seminar Hall in the D Block. The event was attended by the Deans, senior faculty members, the Heads of the Departments, the Coordinators, and other faculty members. The lecture on the Learning Outcome based Curricular Framework was delivered by Professor Alak K Buragohain, Chairperson (Academic). Professor Anuradha Devi, the Coordinator of the IQAC extended a warm welcome to the faculty members and explained the purpose and the importance of the Lecture program. Professor Devi stated that while the RGU is introducing the course curriculum in the next semester beginning from August 2022 as per the policy guidelines of the NEP 2020, there is an urgent need for developing our undergraduate course curricula with provisions for multiple exit and entry with an emphasis on multidisciplinary and skill and value- added courses. She also emphasized on developing the course curricula following the principles of the Learning Outcome based Curricular Framework (LOCF) as stipulated by the UGC. Professor Devi welcomed Professor A.K.Buragohain, Chairperson (Academic) for his presentation and called upon the faculty members to come up with their queries, observations and suggestions. Professor Buragohain started his lecture with a brief historical background on the concept of Outcome Based Education and presented an elaborate discussion on Benjamin Bloom’s Taxonomy of Educational Objectives: The Classification of Educational Goals (1956 and the subsequent editions from 2001 till 2019). The three basic domains of Learning Outcome, viz., cognitive, psychomotor and affective were explained with examples of the Action Verbs prescribed in the later editions of Bloom’s Taxonomy to measure specifically the learning outcomes at the different levels of learning in each of the domains. Relating the basic concepts and the principles of learning outcomes of Bloom’s Taxonomy with the guidelines on Learning Outcome based Curricular Framework (LOCF) of the UGC, Professor Buragohain stated that the philosophy underpinning the LOCF is derived from Bloom’s Taxonomy of Educational Goals. Professor Buragohain explained the basic objectives of the LOCF, the basis of the curriculum planning and development at the graduate level in alignment with the expected mappable and measurable learning outcome, the definitions of Graduate Aptitudes, the concepts of Qualification Descriptor and other related aspects of the LOCF. There was a detailed explanation and discussion on how to work out the Program Outcome and the Course Outcome in concordance with the Qualification Descriptors and the Graduate Aptitudes. The program was followed by an active Q & A session.
