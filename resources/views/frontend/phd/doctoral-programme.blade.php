                                        <p>16.1. Evaluation of Performance in the Course Work</p>
                                        <p>
                                            A Ph.D. student shall be required to obtain a minimum of 55% or its
                                            equivalent grade in the UGC 10-point scale in the Course Work to be eligible
                                            to continue in the programme and carry out the research work generally as
                                            per the approved synopsis and shall submit her/his thesis after due
                                            completion of all formalities as stipulated in the clauses below.
                                        </p>
                                        <p>
                                            16.2. Semester End Evaluation of the Progress of Research Work
                                        </p>
                                        <p>
                                            At the end of the Semester, the DRC shall assess and evaluate the progress
                                            of the work of a Ph.D. student. If the work is found to be satisfactory, the
                                            student shall be recommended for proceeding to the next Semester. In the
                                            event of unsatisfactory Semester End progress for two consecutive Semesters,
                                            the DRC may recommend corrective measures. Should the Ph.D. student fail to
                                            implement the suggested advice(s), and the DRC having exhausted every scope
                                            to enable a Ph.D. student may with justifiable reasons recommend
                                            cancellation of the Registration of a student to the URC. On completion of
                                            the research work, the DRC shall evaluate the complete work and may
                                            recommend a student for Pre-Thesis Submission before the DRC covering the
                                            entire progress of the research work.
                                        </p>
                                        <p>16.3. Pre-Thesis Submission</p>
                                        <p>
                                            A Ph.D. student on being recommended by the DRC, shall present the entire
                                            research work in the Pre-Thesis Submission Seminar before the DRC.
                                        </p>
                                        <p>
                                            Having satisfied the DRC on the research work assigned to the Ph.D. student,
                                            and the Title of the Ph. D. Thesis, fulfilment of the mandatory requirement
                                            of publishing at least one (01) research article in peer reviewed, SCOPUS
                                            indexed/ UGC CARE listed Research Journals and presentation of the research
                                            work in two national/international Conferences/Seminars, the Ph. D. student
                                            shall be required to submit to the DRC the Abstract of the research for
                                            further action by the DRC.
                                        </p>
                                        <p>16.4. Submission of Abstract of the Thesis</p>
                                        <p>
                                            The Ph. D. student shall be required to submit eight (08) copies of the
                                            Abstract in hard as well as soft forms of her/his doctoral research to the
                                            DRC within two (02) months of the successful Pre-Thesis Submission Seminar.
                                            The Abstract may be written approximately in Fifteen Hundred (1500) words.
                                        </p>
                                        <p>
                                            The DRC, having been satisfied with the Abstract, shall forward it to the
                                            URC for approval; if the DRC is not satisfied, it will advise the student to
                                            implement the corrective measures and resubmit.
