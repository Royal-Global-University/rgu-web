                                        <p><strong class="text-primary">3. Introduction</strong></p>
                                        <p>
                                            RGU promotes research in every discipline in the fundamental areas of the
                                            disciplines and research in the applied aspects of knowledge as a core
                                            activity of the University. Each Department of RGU has identified thrust
                                            areas of research with emphasis on broad multi and interdisciplinary
                                            perspectives and approaches to address the fundamental and applied problems
                                            in the global context. Besides, RGU has special focus on addressing the
                                            issues and problems of the northeast region through research. The Ph.D.
                                            program of RGU is to provide a platform for facilitating the stated research
                                            through training and scientific exploration.
                                        </p>

                                        <p><strong class="text-primary">
                                                4. Eligibility Criteria for Admission into the Ph.D. Programme</strong>
                                        </p>
                                        <p>
                                            Candidates applying for admission into the Ph.D. Programme of the University
                                            are required to fulfil any one of the following eligibility criteria:
                                        </p>
                                        <p>4.1. A Candidate who successfully completed:</p>
                                        <p></p>
                                        <p>
                                            4.1.1. One year Master’s degree programme after a 4-year Bachelor’s Degree
                                            with minimum 55% marks or in equivalent grade ‘B’ in the UGC 10-point scale
                                            in subject specified by the relevant Department/Centre of the University.
                                        </p>
                                        <p>Or</p>
                                        <p>
                                            4.1.2 Two year Master’s Degree after a 3-year Bachelor’s Degree with at
                                            least 55% marks in aggregate or an equivalent grade ‘B’ in the UGC 10- point
                                            scale in subject specified by the relevant Department/Centre of the
                                            University.
                                        </p>
                                        <p>Or</p>
                                        <p>
                                            4.1.3. Four year Bachelor’s Degree with Research with a minimum CGPA of 7.5
                                            in a 10-point scale in subject specified by the relevant Department/Centre
                                            of the University.
                                        </p>
                                        <p>Or</p>
                                        <p>
                                            4.1.4. A Degree equivalent to any of the above Degrees in a subject
                                            specified by the relevant Department/Centre of the University from a foreign
                                            educational institution which is accredited by an Assessment and
                                            Accreditation Agency approved, recognized, or authorized by an authority,
                                            established or incorporated under a law in its home country or any other
                                            statutory authority in that country to assess, accredit or assure quality
                                            and standards of educational institutions.
                                        </p>
                                        <p>
                                            4.1.5. An M.Phil. Degree with minimum 55% marks in aggregate or its
                                            equivalent grade in the UGC 10-point scale. A Degree equivalent to any of
                                            the above Degrees in subject specified by the relevant Department/Centre of
                                            the University from a foreign educational institution which is accredited by
                                            any Assessment and Accreditation Agency which is approved, recognized or
                                            authorized by an authority, established or incorporated under a law in its
                                            home country or any other statutory authority in that country to assess,
                                            accredit or assure quality and standards of educational institutions.
                                        </p>
                                        <p>
                                            4.1.6. Candidate who is a member of the Institute of the Chartered
                                            Accountants of India/ the Institute of Company Secretaries of India/ the
                                            Institute of Cost Accountants and Management of India or equivalent foreign
                                            professional body having Bachelor’s Degree with not less than 60% marks or
                                            equivalent CGPA.
                                        </p>
                                        <p>
                                            4.2. Relaxation of Marks in Eligibility Criteria Applicants belonging to the
                                            SC/ST/OBC (Non-Creamy Layer)/ Differently Abled/Economically Weaker Section
                                            (EWS)shall be entitled to a 5% relaxation in marks or an equivalent
                                            relaxation in grades.
                                        </p>

                                        <p><strong class="text-primary">5. Admission Process</strong></p>
                                        <p>
                                            5.1. Admission into the Ph.D. programme takes place twice a year – once in
                                            the Autumn Semester (November-December) and another in the Summer Semester
                                            (July-August).
                                        </p>
                                        <p>
                                            5.2. The Notifications for admission into the Ph.D. programme are advertised
                                            in the national and regional newspapers besides in the University website.
                                        </p>
                                        <p>
                                            5.3. Candidates seeking admission shall submit the application online
                                            through the University website /offline through the Admission Office of the
                                            University along with all documents (scanned /hard copies depending on the
                                            mode of submission of the application) and pay the prescribed Admission
                                            Fees.
                                        </p>
                                        <p>
                                            5.4. The applicant shall submit a concise Statement on the proposed research
                                            work in 1000 words.
                                        </p>
                                        <p>
                                            5.5. The DRCs shall prepare a tentative panel of Ph.D. Guidesfor research
                                            supervision in areas of theirspecialization which shall be uploaded in the
                                            departmental websites of the University.
                                        </p>

                                        <p><strong class="text-primary">6. Selection Procedure</strong></p>
                                        <p>
                                            6.1. The respective DRCs shall screen the applications and the applicants
                                            fulfilling the eligibility criteria as specified in these Regulations and
                                            also satisfying the UGC Notification No. F.4—1 (UGC – NET Review
                                            Committee)/2024 (NET)/140648, Dated - 28th March 2024 will be considered for
                                            admission. The candidates will be categorised as follows:
                                        </p>
                                        <p></p>
                                        <p>
                                            6.1.1. Those candidates who have qualified NET with fellowship shall get
                                            First Preference.
                                        </p>
                                        <p>
                                            6.1.2. The candidates who have qualified under NET - LS and NET will also be
                                            duly considered.
                                        </p>
                                        <p>
                                            6.1.3. All shortlisted candidates shall appear for Oral interaction.
                                        </p>
                                        <p>OR</p>
                                        <p>
                                            6.1.4. The University may also conduct its own entrance examinations for
                                            admission in to various Ph.D programme.
                                        </p>
                                        <p>
                                            6.1.5. The final merit list of the selected candidates shall be made as per
                                            the UGC Notification of 28th March 2024 giving 70% weightage to the NET
                                            scores or exam conducted by the University and 30% to the interview/viva-
                                            voce.
                                        </p>
                                        <p>
                                            6.2. The selected candidates shall be admitted into the Ph.D. programme on
                                            approval of the URC based on the recommendation of the respective DRCs under
                                            intimation to the Academic Council.
                                        </p>

                                        <p><strong class="text-primary">7. Registration</strong></p>
                                        <p>
                                            7.1. The DRCs shall recommend to the University for Registration of the
                                            selected applicants after due consideration.
                                        </p>
                                        <p>
                                            7.2. The approval for Registration by the URC shall be communicated to the
                                            Registrar, Dean R&amp;D and CoE through a comprehensive list of the approved
                                            students of all the Departments/Centres for enrolment of the students in the
                                            Ph.D. Programme under due intimation to the Academic Council.
                                        </p>
                                        <p>
                                            7.3. The registered Ph.D. student shall be required to Renew the
                                            Registration at every semester within the stipulated date.
                                        </p>
                                        <p>
                                            7.4. A student who fails to complete the stipulated research work and fulfil
                                            all requirements for submission of the thesis to the Controller of
                                            Examination within the specified period of her/his registration, shall be
                                            required to Re-Registrar subject to acceptance of the justification of the
                                            student for the failure and approval of the URC which shall be required to
                                            be duly intimated to the Academic Council.
                                        </p>

                                        <p><strong class="text-primary">8. Cancellation of Registration</strong></p>
                                        <p>
                                            The Registration of a Ph.D. student shall be cancelled by the URC under
                                            intimation to the Academic Council on recommendation of the DRC for any one
                                            of the following lapses.
                                        </p>
                                        <p></p>
                                        <p>8.1. Non-payment of fees for two consecutive semesters.</p>
                                        <p>
                                            8.2. Failure to complete the Coursework successfully within one year from
                                            the date of Registration for the Full-Time students and within two years
                                            from the date of Registration for the Part-Time students. A minor relaxation
                                            may be admissible under special circumstance.
                                        </p>
                                        <p>
                                            8.3. Failure to submit and justify a Synopsis within one semester after
                                            successfully completing the Coursework in the case of a Full-Time student
                                            and within two semesters in the case of a Part-Time student.
                                        </p>
                                        <p>
                                            8.4. Failure to submit the Progress Reports within the stipulated period to
                                            the DRC.
                                        </p>
                                        <p>
                                            8.5. If a Full- Time student remains absent for a continuous period of four
                                            weeks without prior intimation and grant of leave of absence by the DRC.
                                        </p>
                                        <p>
                                            8.6. If the academic progress of the student is found to be unsatisfactory
                                            by the Students Research Committee and the DRC.
                                        </p>
                                        <p>
                                            8.7. If a student isfound to be involved in act of misconduct, plagiarism,
                                            academic dishonesty, or indiscipline involving violation of the University
                                            Rules.
                                        </p>
                                        <p>8.8. If a student leaves the Ph.D. programme.</p>

                                        <p><strong class="text-primary">9. Duration of the Ph.D. Programme</strong></p>
                                        <p>
                                            9.1. A Ph.D. student shall be required to complete the coursework and
                                            research for the Ph.D. programme and submit the thesis within a minimum
                                            period of three years and a maximum period of six years from the date of
                                            registration.
                                        </p>
                                        <p>
                                            9.2. Under exceptional conditions and subject to the approval of the URC on
                                            the recommendation of the DRC, the period for completion of the Ph.D. work
                                            may be granted an extension for maximum two years for which the student
                                            shall be required to Re-Register for the granted extension period.
                                        </p>
                                        <p>
                                            9.3. Female students and students with more than 40% disabilities may be
                                            granted an extension of two more additional years.
                                        </p>
                                        <p>
                                            9.4. Female students shall be entitled to maternity Leave/Childcare Leave
                                            for a maximum period of 240 days during the duration of the Ph.D. programme.
                                        </p>
                                        <p></p>
                                        <p><strong class="text-primary">10. Residency Period</strong></p>
                                        <p>
                                            10.1. A Full-Time Ph.D. student shall be required to be present throughout
                                            the Semester in the concerned Department/Centre.
                                        </p>
                                        <p>
                                            10.2. A Part-Time Ph.D. student shall be required to be present in the
                                            concerned Department/Centre for the full semester to attend the coursework
                                            and subsequently shall be required to remain in regular coordination with
                                            the respective Guide and the Co-guide during the entire duration of the
                                            programme.
                                        </p>

                                        <p><strong class="text-primary">11. Leave Rules</strong></p>
                                        <p>
                                            11.1. The attendance and leave of a student shall be recorded in the
                                            concerned Department /Centre duly endorsed by the Students Research
                                            Committee (SRC) which shall be provided to the DRC/URC as and when required
                                            along with the Progress Report of the student.
                                        </p>
                                        <p>
                                            11.2. A Ph. D. student shall be allowed to avail Casual Leave of 8 days per
                                            year for exigencies and 6 days for medical reasons. Such leaves shall be
                                            granted by the DRC on recommendation of the Guide of the student.
                                        </p>
                                        <p>
                                            11.3. A full time Ph.D. student shall not be entitled to avail Semester
                                            Break, Summer, and Winter Vacations.
                                        </p>
                                        <p>
                                            11.4. A Ph.D. student shall be granted Duty Leave for research related work
                                            in the field, libraries, and laboratories of institutions outside the
                                            University for durations as recommended by the Guide and duly endorsed by
                                            the SRC and the DRC.
                                        </p>
                                        <p>Note: The unutilized leave shall not accumulate.</p>

                                        <p><strong class="text-primary">12. Coursework</strong></p>
                                        <p>
                                            12.1. A Ph.D. student must successfully complete a 12 Credit course during
                                            the first two semesters comprising of two compulsory courses on Research
                                            Methodology and Research and Publication Ethics and a subjectspecific course
                                            prescribed by the concerned SRC and DRC.
                                        </p>
                                        <p>
                                            12.2. A Ph.D. student must obtain a minimum of 55% in each course/
                                            equivalent grade in 10- point scale to qualify for continuing the Ph. D.
                                            research.
                                        </p>
                                        <p>
                                            12.3. A Ph.D.student shall be required to participate in training programmes
                                            organised by the Department/Centre on pedagogy/ Research Report/Paper
                                            Writing and may be given teaching assignments for specific duration by the
                                            Guide under intimation to the DRC.
                                        </p>
                                        <p>
                                            12.4. A Ph.D. student may be prescribed to do additional course(s) by the
                                            DRC on recommendation of the SRC when such courses are found necessary for
                                            the research work of the student.
                                        </p>
                                        <p>
                                            12.5. If a student is allowed to avail course(s) at any University within
                                            the country and abroad under some Student Exchange/Short Term Study
                                            programme, relevant to her/his research work on recommendation of the SRC
                                            and the DRC, the Credit earned by the student in that coursework shall be
                                            transferred to RGU. In the case of those coursework being not evaluated at
                                            the University where she/he availed the coursework, the Department of RGU
                                            may do the evaluation for its equivalent Credit and the earned Credit shall
                                            be accounted for in the student’s academic record.
                                        </p>

                                        <p>13. Ph. D. Guide and Co-Guide</p>
                                        <p><strong class="text-primary">13.1. Eligibility</strong></p>
                                        <p>
                                            13.1.1. A regular, full time faculty member who is a Professor or an
                                            Associate Professor having at least five (05) research publications in peer
                                            reviewed journals shall be eligible to become a Guide for a Ph.D. student.
                                        </p>
                                        <p>
