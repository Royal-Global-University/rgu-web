                                            or more/resigns from her or his position in the University/on medical reason
                                            or expires/or for any other unforeseen reasons or circumstances, the SRC and
                                            the DRC shall recommend a new Guide to the URC for consideration of approval
                                            followed by reporting to the Academic Council for the final acceptance.
