                                            b. Associate Professor- six (06)<br /><br />
                                            c. Assistant Professor -four (04)<br /><br />
                                        </p>
                                        <p>
                                            13.2.2. A Ph.D. student may be allotted her/his Guide on recommendation of
                                            the SRC, the DRC and the URC subject to approval of the Academic Council.
                                        </p>
                                        <p>
                                            13.2.3. For effective conduct of research, a Co-Guide is required, however
                                            the same may be waived on a case - to - case basis.
                                        </p>
                                        <p>
                                            13.2.4. When the research work is interdisciplinary/multidisciplinary in
                                            nature, allotment of a Co-Guide to a Ph.D. student from within the
                                            University or from other recognized Universities/Higher Education
                                            Institutions/Research Institutions may be permissible.
                                        </p>
