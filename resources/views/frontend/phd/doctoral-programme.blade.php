                                            Assistant Professor having at least three (03) research publications in peer
                                            reviewed journals shall be eligible to guide a Ph.D. student.
                                        </p>
                                        <p>
                                            13.1.3. Faculty members from other Universities/Higher Education
                                            Institutions fulfilling the eligibility criteria stipulated in 13.1.1 and
                                            13.1.2 may be on recommendation of the DRC, considered as Co-Guides of Ph.D.
                                            student subject to approval of the Academic Council.
                                        </p>
                                        <p>
                                            13.1.4. Scientists/scholars from Research Institutions approved/recognized
                                            by the Government of India/the State Government of Assam, serving in
                                            positions equivalent to the positions of Professor/Associate
                                            Professor/Assistant Professor/ Professor of Practice and fulfilling the
                                            eligibility criteria mentioned at 13.1.1 and 13.1.2 shall be considered as
                                            Co-Guide of a Ph.D. student on recommendation of the DRC.
                                        </p>
                                        <p>
                                            13.1.5. Faculty members who is serving as Emeritus Professor/Advisor after
                                            superannuation from regular service shall be eligible to act as Ph.D. Guides
                                            or Co-Guidestill the age of seventy (70) years.
                                        </p>
