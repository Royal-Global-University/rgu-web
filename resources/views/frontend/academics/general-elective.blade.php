              <h2 class="headd3 pt-4 fw-bold text-dark">
                Generic & Discipline Specific Electives
              </h2>
              <p class="para1 text-dark" style="text-align: justify"></p>
              <p class="para1 text-dark">
                <strong>Generic Electives (GE)</strong> are those subjects which are
                to be opted by students of a department offered by other departments
                of the University. Generic Electives are offered only to undergraduate
                students. A student has to opt and study two GE courses from 1st to
                4th Semester comprising a total of 24 credits.
              </p>
              <p class="para1 text-dark">
                For Example, a student of B.A. Political Science (H) can choose one
                course from Economics and another from Fine Arts. A basket of courses
                is available to students from all the schools of the University. As
                specified in the CBCS system and suggested by UGC, any student wishing
                to earn extra credits, over and above the normal credits, can
                additionally opt for 2 GE courses in the last 2 semesters and this
                will also be reflected in their transcripts.
              </p>
              <p class="para1 text-dark">
                <strong>Discipline Specific Electives (DSE)</strong> are elective
                courses related to a program opted by a student aimed to enhance
                students’ in-depth subject knowledge and can be chosen from a basket
                of many subjects offered by the department. The DSE subjects for UG
                courses are offered in last two semesters while PG students have to
                choose 1 to 3 courses in every semester depending upon the courses
                offered in the program. For Example, a B.Sc. (H) Chemistry student can
                opt for subjects on Organic or Inorganic or Physical Chemistry or any
                other subjects offered by the Department of Chemistry.
              </p>
