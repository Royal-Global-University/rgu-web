        <h3 class="headd3 text-dark fw-bold kd-split-text pt-2">
          About the Department
        </h3>
        <p class="text-dark para1" style="text-align: justify;">
          The Department of Economics, The Assam Royal Global University,
          established in the year 2017, stands as a paragon of scholarly
          excellence and creativity committed to advancing the frontier of
          economic knowledge through specialized research and targeted thrust
          areas. The Department offers undergraduate, postgraduate, and doctoral
          programs With distinguished faculty members, renowned for their
          expertise in Microeconomics, Macroeconomics, Econometrics, International
          Economics and Gender Economics, the department is dedicated to exploring
          critical issues such as Economic Policy, Financial Markets, Development
          Economics, and Behavioral Economics. Its thrust areas encompass
          cutting-edge research in Environmental Economics, Digital Economy,
          Health Economics, Current Socio- Economic Issues, reflecting a dynamic
          response to contemporary global challenges. Our econometrics and
          advanced statistical methods with big data analytics, prepare the
          students to tackle complex economic issues with precision and insight.
          Both Graduate and Post Graduate students benefit from mentorship and
          opportunities to engage in research projects. Regular Seminars,
          Workshop, Conferences, Industrial Visit and Socio-economic Survey keep
          the department with an enriched academic environment. Faculty and
          students actively publish in top-tier journals, showcasing their work on
          global economic, national and local issues. The department has a fair
          placement record, with graduates often securing positions in academia,
          government, and the private sector. Additionally, the incorporation of
          various co-curricular activities by the Department into the educational
          journey of the students prepares them to achieve an all-round
          development of their personality.
