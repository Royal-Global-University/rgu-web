      <div class="p-2">
        <h3 class="headd3 text-dark fw-bold kd-split-text pt-2">
          About the Department
        </h3>
        <p class="text-dark para1" style="text-align: justify;">
          Department of mathematics is a constituent part of Royal School of
          Applied & Pure Sciences was formed in July, 2017 under The Assam Royal
          Global University, Guwahati. The department is presently offering
          courses like B.Sc. Mathematics, M.Sc. Mathematics and Ph.D. in
          Mathematics. The department is blessed with 4 senior experienced faculty
          members along with 9 young, energetic and dedicated faculty members who
          leave no stone unturned to present the aspiring students, the principles
          and concepts of mathematics. All the faculty members of the department
          are actively involved in teaching and research in different thrust areas
          like Fluid dynamics, Mathematical Modelling, Functional Analysis, Fuzzy
          Mathematics, Integro-differential Equations, Mathematical Education,
          Statistical and Data Analysis and Multidisciplinary Research. The course
          learning outcomes of Mathematics are aimed at fascinating the learners
          to acquire knowledge, skills, understanding, values, attributes and
          academic standards. Besides imparting regular courses, the department is
          also preparing the student for higher study in reputed institutions by
          providing JAM coaching to UG students and NET/Gate coaching to PG
          students. For the betterment of the students, the department often
          organized seminar, workshop, conference, webinar, motivational talk,
          guest lectures by renowned mathematician, field trips, annual events
          like National Mathematics Day, National Science Day, Pi-Day and also
          involve the students in sports, club activities and corporate
          interaction. The students after completing their UG and PG degrees in
          Mathematics can take up their career paths in Data Analysis, Actuarial
          Science, Finance and Banking, Computer Science and Information
          Technology, Engineering and Applied Sciences, Teaching, Government and
          Public Sector jobs, Entrepreneurship, and in Academic Research.
        </p>
