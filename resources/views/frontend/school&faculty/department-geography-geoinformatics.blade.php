      <div class="p-2">
        <h3 class="headd3 text-dark fw-bold kd-split-text pt-2">
          About the Department
        </h3>
        <p class="text-dark para1" style="text-align: justify;">
          The Department of Geography at The Assam Royal Global University in
          Guwahati is renowned for its comprehensive integration of physical and
          human geography, providing a diverse curriculum for undergraduate and
          postgraduate students. The Department runs various programmes such
          BA/B.Sc Geography, MA/M.Sc Geography, M.Sc Geoinformatics and PhD
          programmes. The courses includes geomorphology, climatology,
          biogeography, urban geography, regional planning, and Remote Sensing and
          GIS. The syllabus includes field-based learning and visit to
          institutions of prominence for exposure visits to the students of the
          department. Moreover the Department of Geography frequently organises
          outreach programs for the students to embed community involvement
          strategies into geographical pursuits and to nurture ethical values and
          empathy among students. The faculties of the department are specialized
          to guide students through inter-disciplinary courses and a balanced
          education that emphasizes field-based learning and the use of advanced
          GIS and remote sensing technologies. Key focus areas such as Physical
          Geography, Environmental Geography, Tourism Geography, Agricultural
          Geography and Migration Studies enable the department to understand the
          pressing issues of man-environment relationship and sustainability.
          Graduates and post graduates are well equipped with critical thinking,
          spatial analysis through GIS and Remote sensing, field based empirical
          research making them well-suited for careers in urban and regional
          planning, environmental management, transportation planning, public
          policy and geospatial technology with strong prospects for further
          academic studies, including PhDs.
        </p>
