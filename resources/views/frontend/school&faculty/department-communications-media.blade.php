      <div class="p-2">
        <h3 class="headd3 text-dark fw-bold kd-split-text pt-2">
          About the Department
        </h3>
        <p class="text-dark para1" style="text-align: justify;">
          There is a popular saying that ‘Communication rules the World’ without
          which the overall development of society is not possible. The Royal
          School of Communications and Media, which is popularly known as (RSCoM)
          comprises two departments namely the Department of Journalism and Mass
          Communication and the Department of Animation. The Royal School of
          Communications and Media focuses on quality media education in the field
          of Media Studies in India with a special focus on the Northeast. The key
          thrust areas of the school include Development Communication &
          Traditional Media, Print Media vs Digital Media, Radio & Television
          Journalism, Advertising, Public Relations and Corporate Communication,
          New Media Technology and Online Journalism, Event Management, and Media
          Management. The core strength of the school lies in its highly qualified
          and experienced faculty members who are drawn from both Academia and
          Industry. We at the Royal School of Communications and Media focus more
          on the industry-academia interface and hands-on practical exposure so
          that our students will be able to understand the changing trends and
          nuances of the media and entertainment industry, which is the
          fastest-growing sector. As per the industry requirement, we train and
          inspire our students in many ways. The classroom environment is so
          conducive where our students learn to question, engage, and articulate
          their ideas rationally. The learning process is enhanced through invited
          lectures and special classes by nationally and internationally prominent
          persons, industry experts, senior academicians, etc. According to the
          latest FICCI report the Indian M&E sector grew by 8% in 2023, reaching
          INR2.3 trillion (US$27.9 billion), 21% above its pre-pandemic levels in
          2019. Digital advertising grew 15% in 2023 and surpassed traditional
          advertising for the first time. The avenues and opportunities that are
          available in the field of Journalism and Mass Communication are numerous
          and diversified. A career in Journalism and Mass Communication is
          lucrative and it offers a plethora of avenues: Reporter, Copy Editor,
          Sub-editor, Editor, Photo Journalist, Video Journalist, Photographer,
          Graphic Designer, News Anchor, Director, Producer, News Editor,
          Cameraman, Screen Writer, Sound Editor, Video Editor, Voice Over Artist,
          Radio Jockey, Video Jockey, Event Manager, Public Relations officer,
          Corporate Communication officer, Brand Manager, Art Director, Ad
          Designer, Data Journalist, Actor, Social Media Manager, Advertising
          Manager, Creative Director, Media Planner, Social Media Officer,
          Information Officer, Communication Officer and many more.
        </p>
