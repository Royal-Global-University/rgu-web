      <div class="p-2">
        <h3 class="headd3 text-dark fw-bold kd-split-text pt-2">
          About the Department
        </h3>
        <p class="text-dark para1">
          The Department of History is one of the premier departments of Royal
          Global University. It has eminent and scholarly faculty with global
          exposure. It is emerging as one of the leading centre of historical
          learning and research in the north-east India. Its objective is to
          initiate innovative research and world class curriculum design with
          industry-oriented pedagogy, to ground historical erudition in the shared
          national culture of compassion, social responsibility and sound ethical
          outlook and to enrich society by producing nationally and
          internationally acclaimed historians and historical works with acute
          transformative potential in the domain of social sciences. In its thrust
          to update its curriculum according to the mandate of New Education
          Policy of the GOI, it has included skill enhancement courses like
          Museology, Archeology, Heritage management, Basic Writing Skills,
          Value-Added Courses as well as mandatory project work in it is
          curriculum. It has MoU with leading academic and research institutions
          which provide internships to our students for quick employability. The
          Department often organizes short trips to historical/archeological
          sites, traditional heritage sites, traditional industrial and handicraft
          sites for firsthand experience of students. Within a short span of its
          existence, it has already produced NET and JRF qualified students,
          officers in Indian Banking Service as well as faculty in reputed
          universities. Some of our students have received admission in reputed
          universities abroad for higher studies. The department has started an
          Outreach programme with the motto ‘Popularizing History, Supplementing
          Syllabus: Using alternative Pedagogy’ to start society-academia
          interface. To facilitate research, and writing skills of our students
          the Department has established History Society – Vrittaanta which
          provide a platform to all students and research scholars of the
          Department for popularizing the discipline, engage in debate and
          discussion in historical themes amongst students and members of the
          larger society. It has also the credit of publishing the first
          Departmental student e-research magazine of the University named
          Anveshan: A Voyage into the Unexplored Past. The Department and its
          faculty strive to produce distinguished students who would be leading
          historical research in the region.
        </p>
