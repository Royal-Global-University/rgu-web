                                April 25, 2024:</b></p>

                        <p class="para1 text-dark px-5">
                            <b>a) Poster Launch Ceremony:</b> The State-Level Research Paper Writing Competition – ViViBha,
                            in partnership with Bharatiya Sikhshan Mandal, was launched during this event. This competition
                            invites participants to submit research papers on topics related to Indian Knowledge Systems,
                            encouraging scholarly engagement and exploration of Indian heritage, culture, and knowledge
                            traditions.
                            <br><br>
                            <b>b) Lecture on Indian Knowledge Systems:</b> Alongside the competition launch, two
                            distinguished lectures were organized:
                            <br>
                            <b>I. Plenary Lecture I:</b> Prof. (Dr.) Kameshwar Shukla, Department of Sanskrit, Gauhati
                            University.<br>
                            <b>Title:</b> Bharatiya Sikhshan Mandal and Indian Knowledge System
                            <br><br>
                            <b>II. Plenary Lecture II:</b> Prof. (Dr.) Uday Dixit, Department of Mechanical Engineering,
                            Head, IKS Center, IIT Guwahati.<br>
                            <b>Title:</b> IKS and NEP 2020
                        </p>

                        <p class="para1 text-dark"><b>3. Interdepartmental Event: Exhibition on Culture and Heritage &
                                Lecture Session with INTACH (May 20-22, 2024)</b></p>

                        <p class="para1 text-dark px-5">
                            <b>A. Three-day Exhibition on "Culture and Heritage"</b><br>
                            The IKS Cell, in association with INTACH and various departments of RGU, organized an exhibition
                            showcasing artifacts, artworks, photographs, and interactive displays, representing diverse
                            cultural aspects, including history, art, architecture, and tourism.
                            <br><br>
                            <b>B. Lecture Session (May 20, 2024)</b><br>
                            <b>Speaker:</b> Ms. Vandana Manchanda, Director, INTACH.<br>
                            <b>Title:</b> Craft Revival and its Socio-Economic Impact on Communities.<br>
                            The lecture explored the resurgence of traditional artisanal practices and the socio-economic
                            impact of craft revival on local communities.
                        </p>

