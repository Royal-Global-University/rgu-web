                            University Grants Commission (UGC), the Indian Knowledge System (IKS) Syllabus for the
                            Undergraduate Course consists of two introductory courses, namely "Introduction to IKS-1" and
                            "Introduction to IKS-2," each worth 3 credits. These courses are taught to undergraduate
                            students during their 1st and 2nd semesters. Currently, more than 1400 students enrolled in the
                            UG 1st and 2nd semester, organized into 20 groups, have completed the IKS-1 foundation course
                            and are presently undergoing the IKS-2 foundation course.</p>

