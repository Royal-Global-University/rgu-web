                                Theatre" by Shri Pranjal Saikia (May 24, 2024)</b></p>

                        <p class="para1 text-dark px-5">
                            <b>Organizer:</b> IKS Cell and DSW, RGU.<br>
                            <b>Speaker:</b> Shri Pranjal Saikia, Sangeet Natak Akademi awardee.<br>
                            The session explored Abhinaya (expressive performance) and its deep roots in folk traditions,
                            storytelling, music, and dance.
                        </p>

                        <p class="para1 text-dark"><b>5. Distinguished Lecture Session (May 27, 2024)</b></p>

                        <p class="para1 text-dark px-5">
