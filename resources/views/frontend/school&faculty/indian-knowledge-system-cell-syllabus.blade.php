                            Travel & Tourism.<br>
                            <b>Speaker:</b> Dr. Nachiket Chanchani, University of Michigan, Ann Arbor, USA.<br>
                            <b>Topic:</b> Decoding India’s Temples and the Challenges of Reconstructing Them.<br>
