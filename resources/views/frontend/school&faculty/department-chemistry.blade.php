      <div class="p-2">
        <h3 class="headd3 text-dark fw-bold kd-split-text pt-2">
          About the Department
        </h3>
        <p class="text-dark para1" style="text-align: justify;">
          The Department of Chemistry, RSAPS is dedicated to teaching both
          fundamental knowledge and practical applications in the chemical
          sciences through a diverse range of research areas. Organic and
          Inorganic synthesis focus on creating organic and inorganic compounds,
          respectively, contributing to new materials, pharmaceuticals, industrial
          applications, and biological applications. Catalysis accelerates of
          chemical reactions using catalysts, essential for efficient and
          sustainable chemical processes. The works on sensors develops sensors
          for detecting and measuring substances in environmental monitoring and
          medical diagnostics. Research in Energy, Environment, and Sustainability
          explores new materials and processes for sustainable energy production,
          environmental remediation, and eco-friendly technologies, addressing
          global challenges with an emphasis on sustainability. Computational
          Chemistry employs the computational methods and simulations to
          understand molecular properties and interactions, predicting
          experimental outcomes and designing new molecules. Chemical Biology, at
          the intersection of chemistry and biology, investigates chemical
          processes within living organisms, designs chemical tools for biological
          studies, advances drug discovery, and elucidates molecular mechanisms of
          diseases in cellular level. The department's goal of achieving
          excellence is to inspire research and developed towards translational
          research that commits to scientific findings and ethical responsibility
          for the betterment of society. The department has provision of special
          laboratories for different areas of organic, inorganic, and physical
          chemistry dedicating to empower students for industry, academia, and
          research. The departmental labs are equipped with modern-day instruments
          for qualitative and quantitative analysis, ensuring alignment with the
          curriculum. Major safety measures such as emergency equipment, proper
          ventilation, and clear guidelines are in place to protect students and
          faculty. Additionally, the department features dedicated classroom
          spaces with audio-visual aids, well-appointed faculty offices as well as
          qualified faculties in all disciplines, a specialized library with
          relevant textbooks, and ample storage facilities for laboratory
          equipment and supplies. Robust networking and connectivity facilitate
          seamless communication among faculty, students, and researchers,
          fostering a collaborative academic environment. Regular maintenance and
          upkeep ensure that all facilities remain in optimal condition,
          reflecting the university's commitment to providing a high-quality
          learning environment that is accessible and inclusive to all students.
        </p>
