      <div class="p-2">
        <h3 class="headd3 text-dark fw-bold kd-split-text pt-2">
          About the Department
        </h3>
        <p class="text-dark para1" style="text-align: justify;">
          The Department of Geology, established in October, 2020 has rapidly
          emerged as a center of academic and research excellence, offering B.Sc.
          (H) Geology aligned with the National Education Policy (NEP) 2020, M.Sc.
          Geology, and PhD programs. The programs are periodically updated to meet
          industry standards and academic requirements, ensuring that students
          receive a comprehensive education that prepares them for successful
          careers. With a strong emphasis on hands-on learning and research, our
          department is equipped with good laboratory facilities featuring a
          comprehensive collection of mineral, rock, and fossil specimens, crystal
          models, and geomorphic models.<br />

          The Department of Geology focuses on a diverse range of research areas,
          leveraging the expertise of its faculty comprising one Professor and
          seven Assistant Professors. Key research domains include Geomorphology,
          Earth and Climate Studies, Ore Geology and Mineral Exploration,
          Sedimentology and Stratigraphy, Volcanology and Petrology,
          Palaeomagnetism and Palaeogeography. These research efforts ensure that
          the department stays at the forefront of geological sciences,
          contributing valuable insights and advancements to the field. Moreover,
          the department nurtures talent and celebrates student achievements,
          fostering a vibrant academic community. Notable accomplishments include
          clearing national entrance exams, winning debates and competitions, and
          showcasing talents in cultural events. This combination of academic
          excellence, research prowess, and student engagement establishes the
          Department of Geology as a premier destination for geological studies
          and research in the region.
        </p>
